public class Exercicio01 {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println("O numero é " + i + " par ");
            } else {
                System.out.println("O numero é " + i + " impar ");
            }
        }
    }
}
